
<h2>OpenGL 3D City Fly Through + Spotlight and Different Textures C++</h2><br />
<p>My Computer Graphics Class CS371 final project. It is a fly through of multiple buildings. The fly through adjusts the gluLookAt() function by adding and incrementing it's parameters then re-rendering the image in the timer() function. Each building face has a normal, so the spotlight lighting feature could be used. The buildings are all different Textures, all different combinations of specucular, diffuse, ambient and shininess to exemplify Brass, Chrome, Jade, Polished, Gold and Ruby. </p>

