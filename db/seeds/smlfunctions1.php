<h2>SML Functions: nth power, smallest of 3 numbers and list member cycle</h2>
<p>Some Standard ML of New Jersey functions. The first function pow(a, b) raises a (real) to the power of b (int). the second function power() does the same thing but using only reals (real to a power of a real). min3(a, b, c) takes 3 integers and returns the smallest one. The final function cycle() takes a list and cycles the first number in a list to the last spot </p>


